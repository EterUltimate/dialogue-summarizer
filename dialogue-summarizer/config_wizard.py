class ConfigWizard:
    """配置向导"""
    
    def __init__(self, config_dir: str = "."):
        self.config_dir = config_dir
        self.env_path = os.path.join(config_dir, ".env")
    
    def load_current_config(self) -> Dict[str, str]:
        """加载当前配置"""
        config = {}
        if os.path.exists(self.env_path):
            with open(self.env_path, 'r', encoding='utf-8') as f:
                for line in f:
                    line = line.strip()
                    if line and not line.startswith('#') and '=' in line:
                        key, value = line.split('=', 1)
                        config[key.strip()] = value.strip()
        return config
    
    def save_config(self, config: Dict[str, str]) -> str:
        """保存配置"""
        try:
            with open(self.env_path, 'w', encoding='utf-8') as f:
                f.write("# Dialogue Summarizer Configuration\n")
                f.write("# Generated by Config Wizard\n\n")
                
                f.write("# ============ Backend Selection ============\n")
                f.write(f"DIALOGUE_BACKEND={config.get('backend', 'ollama')}\n\n")
                
                f.write("# ============ Ollama Settings ============\n")
                f.write(f"OLLAMA_BASE_URL={config.get('ollama_base_url', 'http://localhost:11434')}\n")
                f.write(f"OLLAMA_EMBEDDING_MODEL={config.get('ollama_embedding_model', 'nomic-embed-text')}\n")
                f.write(f"OLLAMA_LLM_MODEL={config.get('ollama_llm_model', 'qwen2.5:7b')}\n\n")
                
                f.write("# ============ OpenAI API Settings ============\n")
                f.write(f"OPENAI_API_KEY={config.get('openai_api_key', '')}\n")
                f.write(f"OPENAI_BASE_URL={config.get('openai_base_url', 'https://api.openai.com/v1')}\n")
                f.write(f"OPENAI_EMBEDDING_MODEL={config.get('openai_embedding_model', 'text-embedding-3-small')}\n")
                f.write(f"OPENAI_LLM_MODEL={config.get('openai_llm_model', 'gpt-4o-mini')}\n\n")
                
                f.write("# ============ Vector Database Settings ============\n")
                f.write(f"VECTORDB_TYPE={config.get('vectordb_type', 'chroma')}\n")
            
            return f"✅ 配置已保存到: {self.env_path}"
        except Exception as e:
            return f"❌ 保存失败: {str(e)}"
    
    def test_ollama_connection(self, base_url: str) -> str:
        """测试Ollama连接"""
        try:
            import requests
            response = requests.get(f"{base_url.rstrip('/')}/api/tags", timeout=5)
            if response.status_code == 200:
                models = response.json().get('models', [])
                model_names = [m.get('name', '') for m in models]
                if model_names:
                    return f"✅ 连接成功！已安装模型:\n" + "\n".join(f"  • {m}" for m in model_names[:10])
                return "✅ 连接成功！暂无已安装模型，请使用 ollama pull 安装模型"
            return f"⚠️ 服务器响应异常: {response.status_code}"
        except requests.exceptions.ConnectionError:
            return "❌ 无法连接到Ollama服务，请确保Ollama正在运行"
        except Exception as e:
            return f"❌ 连接失败: {str(e)}"
    
    def test_openai_connection(self, api_key: str, base_url: str) -> str:
        """测试OpenAI API连接"""
        if not api_key:
            return "⚠️ 请输入API Key"
        
        try:
            import requests
            headers = {
                "Authorization": f"Bearer {api_key}",
                "Content-Type": "application/json"
            }
            url = f"{base_url.rstrip('/')}/models"
            response = requests.get(url, headers=headers, timeout=10)
            
            if response.status_code == 200:
                return "✅ API连接成功！"
            elif response.status_code == 401:
                return "❌ API Key无效"
            else:
                return f"⚠️ 服务器响应: {response.status_code}"
        except Exception as e:
            return f"❌ 连接失败: {str(e)}"


