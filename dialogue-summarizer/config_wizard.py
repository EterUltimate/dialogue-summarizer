def run_config_wizard():
    """运行配置向导"""
    demo = create_config_wizard_ui()
    demo.launch(
        server_name="127.0.0.1",
        server_port=7861,
        share=False,
        show_error=True
    )


if __name__ == "__main__":
    run_config_wizard()
